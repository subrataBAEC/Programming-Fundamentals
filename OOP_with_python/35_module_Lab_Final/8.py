"""
You need to write a python lambda function to tell whether a number is even or not. 
If it is even, return “Yes” otherwise “No”.

O/P:
print(even_odd(5))
Output:
No

"""

even_odd=lambda n:"Yes" if (n%2==0) else "No"
print(even_odd(5))
